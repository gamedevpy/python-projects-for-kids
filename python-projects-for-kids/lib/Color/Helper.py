class Helper():

	def __init__(self):
		self.red    = (255, 0, 0)
		self.orange = (255, 127, 0)
		self.yellow = (255, 255, 0)
		self.green  = (0, 255, 0)
		self.blue   = (0, 0, 255)
		self.violet = (127, 0, 255)
		self.brown  = (102, 51, 0)
		self.black  = (0, 0, 0)
		self.white  = (255, 255, 255)

	def getRed(self):
		return self.red

	def getOrange(self):
		return self.orange

	def getYellow(self):
		return self.yellow

	def getGreen(self):
		return self.green

	def getBlue(self):
		return self.blue

	def getViolet(self):
		return self.violet

	def getBrown(self):
		return self.brown

	def getBlack(self):
		return self.black

	def getWhite(self):
		return self.white